package com.staffbase.employee_record_system.dto;

import jakarta.validation.constraints.NotBlank;

public record ChangePasswordRequest(
                @NotBlank(message = "Current password is required") String currentPassword,
                @NotBlank(message = "New password is required") @jakarta.validation.constraints.Size(min = 6, message = "New password must be at least 6 characters") String newPassword,
                @NotBlank(message = "Confirm password is required") String confirmPassword) {
}



